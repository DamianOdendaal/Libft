#include "libft.h"

void	ft_putnbr(int n)
{
	long int a;

	a = (long int)n;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (a < 0)
	{
		ft_putchar('-');
		a *= -1;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else 
		ft_putchar(a + '\0');
}
