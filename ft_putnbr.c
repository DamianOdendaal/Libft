#include "libft.h"

void	ft_putnbr(int n)
{

	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n == 2147483647)
		ft_putstr("2147483647");
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else 
		ft_putchar(n + '\0');
}
