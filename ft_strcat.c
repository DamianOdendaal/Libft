#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t i;

	i = 0;
	
	while (s2[i])
	{
		s1 = (char *)malloc(sizeof(char *) * i);
		s1[i] = s2[i];
		i++;
	}
	return (s1);
}
